package t9.core.funcs.specialflag.data;

public class T9SpecialFlag {
  private int seqId;
  private String flagSort;
  private String flagCode;
  private String flagDesc;
  
  public int getSeqId() {
    return seqId;
  }
  public void setSeqId(int seqId) {
    this.seqId = seqId;
  }
  public String getFlagSort() {
    return flagSort;
  }
  public void setFlagSort(String flagSort) {
    this.flagSort = flagSort;
  }
  public String getFlagCode() {
    return flagCode;
  }
  public void setFlagCode(String flagCode) {
    this.flagCode = flagCode;
  }
  public String getFlagDesc() {
    return flagDesc;
  }
  public void setFlagDesc(String flagDesc) {
    this.flagDesc = flagDesc;
  } 
}
