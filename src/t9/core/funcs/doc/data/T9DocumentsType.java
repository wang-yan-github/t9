package t9.core.funcs.doc.data;
public class T9DocumentsType {

  private int seqId;
  private String documentsName;
  private int flowType;
  private String documentsFont;
  private String documentsWordModel;
  private int typeNo;
  
  public int getTypeNo() {
    return typeNo;
  }
  public void setTypeNo(int typeNo) {
    this.typeNo = typeNo;
  }
  public int getSeqId() {
    return seqId;
  }
  public void setSeqId(int seqId) {
    this.seqId = seqId;
  }
  public String getDocumentsName() {
    return documentsName;
  }
  public void setDocumentsName(String documentsName) {
    this.documentsName = documentsName;
  }
  public int getFlowType() {
    return flowType;
  }
  public void setFlowType(int flowType) {
    this.flowType = flowType;
  }
  public String getDocumentsFont() {
    return documentsFont;
  }
  public void setDocumentsFont(String documentsFont) {
    this.documentsFont = documentsFont;
  }
  public String getDocumentsWordModel() {
    return documentsWordModel;
  }
  public void setDocumentsWordModel(String documentsWordModel) {
    this.documentsWordModel = documentsWordModel;
  }
  
}
