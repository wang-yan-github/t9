package t6.fis.funcs.acsetparam.data;

public class T9AcParam {
  /**唯一ID**/
  private int    seqId; 
  /**会计年度**/
  private String acctYear;
  /**参数分类**/
  private String paraSort;
  /**参数名称**/
  private String paraDesc;
  /**参数值**/
  private String paraValue;
  /**参数值说明**/
  private String description; 
  /**参数名字**/
  private String paraName;
  public int getSeqId() {
    return seqId;
  }
  public void setSeqId(int seqId) {
    this.seqId = seqId;
  }
  public String getAcctYear() {
    return acctYear;
  }
  public void setAcctYear(String acctYear) {
    this.acctYear = acctYear;
  }
  public String getParaSort() {
    return paraSort;
  }
  public void setParaSort(String paraSort) {
    this.paraSort = paraSort;
  }
  public String getParaDesc() {
    return paraDesc;
  }
  public void setParaDesc(String paraDesc) {
    this.paraDesc = paraDesc;
  }
  public String getParaValue() {
    return paraValue;
  }
  public void setParaValue(String paraValue) {
    this.paraValue = paraValue;
  }
  public String getDescription() {
    return description;
  }
  public void setDescription(String description) {
    this.description = description;
  }
  public String getParaName() {
    return paraName;
  }
  public void setParaName(String paraName) {
    this.paraName = paraName;
  }

}
